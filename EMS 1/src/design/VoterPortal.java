/* ------------------------------------------------------------------
   fetch list_constituencies and fill jComboBox2
   each line: CODE;NAME   → show  CODE - NAME
   ------------------------------------------------------------------ */
private void loadConstituencies() {
    try (Socket s = new Socket("127.0.0.1", 54321);
         PrintWriter out = new PrintWriter(s.getOutputStream(), true);
         BufferedReader in = new BufferedReader(new InputStreamReader(s.getInputStream()))) {

        out.println("list_constituencies");
        DefaultComboBoxModel<String> model = new DefaultComboBoxModel<>();
        String line;
        while (!(line = in.readLine()).equals("END")) {
            if (line.startsWith("ERROR")) {
                JOptionPane.showMessageDialog(this, line);
                return;
            }
            String[] p = line.split(";", 2);
            model.addElement(p[0] + " - " + p[1]);
        }
        jComboBox2.setModel(model);

    } catch (IOException ex) {
        JOptionPane.showMessageDialog(this, "Server error: " + ex.getMessage());
    }
}

