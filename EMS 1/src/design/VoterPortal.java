/* ------------------------------------------------------------------
   Called when the window opens – fills labels on Home tab
------------------------------------------------------------------ */
private void loadHomeData() {
    try (Socket s  = new Socket("127.0.0.1", 54321);
         PrintWriter out = new PrintWriter(s.getOutputStream(), true);
         BufferedReader in = new BufferedReader(new InputStreamReader(s.getInputStream()))) {

        out.println("count_candidates;");          // <‑‑ send command
        String resp = in.readLine();               // e.g., OK:17

        if (resp != null && resp.startsWith("OK:")) {
            String num = resp.substring(3);        // strip “OK:”
            Candidate_count.setText(num);          // label on Home tab
        } else {
            Candidate_count.setText("0");          // fallback
        }

    } catch (IOException ex) {
        Candidate_count.setText("‑‑");             // couldn’t connect
    }
}
