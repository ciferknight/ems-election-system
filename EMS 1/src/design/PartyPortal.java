private void loadReportTable() {
    try {
        Socket socket = new Socket("127.0.0.1", 54321);
        PrintWriter out = new PrintWriter(socket.getOutputStream(), true);
        BufferedReader in = new BufferedReader(new InputStreamReader(socket.getInputStream()));

        out.println("fetch_report");

        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0); // Clear old rows

        // Optional: reset column headers in case JTable got reused
        model.setColumnIdentifiers(new String[] {
            "Party Name", "Party Symbol", "Candidate Name", "Candidate ID", "Votes"
        });

        String response;
        while ((response = in.readLine()) != null && !response.equals("END")) {
            String[] row = response.split(";");
            model.addRow(row);
        }

        socket.close();
    } catch (IOException e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "Failed to load report data from server.");
    }
}
