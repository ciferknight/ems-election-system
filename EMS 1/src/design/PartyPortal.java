private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {
    String partyId = jTextField5.getText(); // Party ID field

    if (partyId.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Please enter Party ID to remove.");
        return;
    }

    try {
        Socket socket = new Socket("127.0.0.1", 54321);
        PrintWriter out = new PrintWriter(socket.getOutputStream(), true);
        BufferedReader in = new BufferedReader(new InputStreamReader(socket.getInputStream()));

        String request = "remove_party;" + partyId;
        out.println(request);

        String response = in.readLine();
        if ("SUCCESS".equals(response)) {
            JOptionPane.showMessageDialog(this, "Party removed successfully!");
        } else if ("NOT_FOUND".equals(response)) {
            JOptionPane.showMessageDialog(this, "Party ID not found!");
        } else {
            JOptionPane.showMessageDialog(this, "Failed: " + response);
        }

        socket.close();
    } catch (IOException e) {
        e.printStackTrace();
    }
}
